DECLARE

    CURSOR C_PERSON IS
        SELECT *
          FROM APPS.XXCALV_VAC_EVENTOS
         WHERE 1 = 1
           AND PERSON_ID = 3133;

BEGIN


    FOR PERSON IN C_PERSON LOOP
    
        DELETE FROM APPS.XXCALV_VAC_EVENTOS_DET
         WHERE 1 = 1
           AND ID_EVENTO = PERSON.ID_EVENTO;
    
    END LOOP;


END;


COMMIT;