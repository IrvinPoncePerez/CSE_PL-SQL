CREATE TABLE ATET_SB_TABLES
(
    TABLE_ID            NUMBER          NOT NULL,
    TABLE_NAME          VARCHAR2(50),
    ATTRIBUTE1          NUMBER,
    ATTRIBUTE2          NUMBER,
    ATTRIBUTE3          NUMBER,
    ATTRIBUTE4          NUMBER,
    ATTRIBUTE5          NUMBER,
    ATTRIBUTE6          VARCHAR2(250),
    ATTRIBUTE7          VARCHAR2(250),
    ATTRIBUTE8          VARCHAR2(250),
    ATTRIBUTE9          VARCHAR2(250),
    ATTRIBUTE10         VARCHAR2(250),
    CREATION_DATE       DATE,
	CREATED_BY          NUMBER,
	LAST_UPDATE_DATE    DATE,
	LAST_UPDATED_BY     NUMBER
);


ALTER TABLE ATET_SB_TABLES
	ADD CONSTRAINT UQ_ATET_SB_TABLES UNIQUE (TABLE_ID)
 USING INDEX ;


CREATE SEQUENCE ATET_SB_TABLES_SEQ
  START WITH 1
  NOCYCLE
  NOCACHE
  NOORDER;
  
  
CREATE OR REPLACE TRIGGER ATET_SB_TABLES_TGR
BEFORE INSERT
   ON ATET_SB_TABLES
   FOR EACH ROW
DECLARE

   var_next_id  NUMBER;

BEGIN

    SELECT ATET_SB_TABLES_SEQ.NEXTVAL
      INTO var_next_id
      FROM dual;        
      
    :NEW.TABLE_ID := var_next_id;

END;