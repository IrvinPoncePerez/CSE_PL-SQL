CREATE OR REPLACE VIEW ATET_PERIODS_UNACCOUNTED_V
AS
SELECT D.REQUEST_ID,
       D.PERIOD_NAME,
       D.ELEMENT_NAME
  FROM (SELECT DISTINCT
               ASST.REQUEST_ID              AS  "REQUEST_ID",
               ASST.PERIOD_NAME             AS  "PERIOD_NAME",
               ASST.ELEMENT_NAME            AS  "ELEMENT_NAME"
          FROM ATET_SB_SAVINGS_TRANSACTIONS     ASST
         WHERE 1 = 1
           AND ASST.ACCOUNTED_FLAG = 'UNACCOUNTED'
         UNION
        SELECT DISTINCT
               ASLT.REQUEST_ID              AS  "REQUEST_ID",
               ASLT.PERIOD_NAME             AS  "PERIOD_NAME",
               ASLT.ELEMENT_NAME            AS  "ELEMENT_NAME"
          FROM ATET_SB_LOANS_TRANSACTIONS       ASLT
         WHERE 1 = 1
           AND ASLT.ACCOUNTED_FLAG = 'UNACCOUNTED') D
 WHERE 1 = 1
 ORDER BY REQUEST_ID;
  