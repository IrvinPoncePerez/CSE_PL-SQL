  NOCACHE
  NOORDER;
  
  
CREATE OR REPLACE TRIGGER ATET_SB_LOANS_TRAN_TGR
BEFORE INSERT
   ON ATET_SB_LOANS_TRANSACTIONS
   FOR EACH ROW
DECLARE

   var_next_id  NUMBER;

BEGIN

    SELECT ATET_SB_LOANS_TRAN_SEQ.NEXTVAL
      INTO var_next_id
      FROM dual;        
      
    :NEW.LOAN_TRANSACTION_ID := var_next_id;

END;