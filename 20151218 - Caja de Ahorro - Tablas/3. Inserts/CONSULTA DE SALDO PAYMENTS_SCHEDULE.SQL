SELECT ASM.EMPLOYEE_NUMBER,
       ASM.EMPLOYEE_FULL_NAME,
       ASPS.PAYMENT_NUMBER,
       ASPS.PAYMENT_AMOUNT,
       ASPR.ENTRY_VALUE,
       ASLT.ENTRY_VALUE,
       ASPS.PAYMENT_CAPITAL,
       ASLT.PAYMENT_CAPITAL,
       ASPS.ATTRIBUTE1,
       ASPS.PAYMENT_INTEREST,
       ASLT.PAYMENT_INTEREST,
       ASPS.ATTRIBUTE2,
       ASPS.PAYMENT_INTEREST_LATE,
       ASLT.PAYMENT_INTEREST_LATE,
       ASPS.ATTRIBUTE3,
       ASPS.ATTRIBUTE6
  FROM ATET_SB_MEMBERS              ASM,
       ATET_SB_LOANS                ASL,
       ATET_SB_PAYMENTS_SCHEDULE    ASPS,
       ATET_SB_PAYROLL_RESULTS      ASPR,
       ATET_SB_LOANS_TRANSACTIONS   ASLT
 WHERE 1 = 1 
   AND ASM.MEMBER_ID = ASL.MEMBER_ID
   AND ASPS.LOAN_ID = ASL.LOAN_ID
   AND ASPR.TIME_PERIOD_ID = ASPS.TIME_PERIOD_ID
   AND ASPR.PAYROLL_RESULT_ID = ASLT.PAYROLL_RESULT_ID
   AND ASPS.LOAN_ID = ASLT.LOAN_ID
--   AND ASPS.PAYMENT_NUMBER = :P_PAYMENT_NUMBER