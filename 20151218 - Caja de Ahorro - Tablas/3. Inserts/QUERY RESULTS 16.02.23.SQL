SELECT ASM.EMPLOYEE_NUMBER,
       ASM.EMPLOYEE_FULL_NAME,
       ASM.PERSON_ID,
       ASL.LOAN_BALANCE,
       ASMA.DEBIT_BALANCE,
       ASMA.CREDIT_BALANCE,
       ASMA.FINAL_BALANCE,
       ASPS.PAYMENT_SCHEDULE_ID,
       ASPS.PAYMENT_NUMBER,
       ASPS.TIME_PERIOD_ID,
       ASPS.PERIOD_NAME,
       ASPS.PAYMENT_AMOUNT,
       ASPS.PAYMENT_CAPITAL,
       ASPS.PAYMENT_INTEREST,
       ASPS.PAYMENT_INTEREST_LATE,
       ASPS.STATUS_FLAG,
       ASPS.ATTRIBUTE1,
       ASPS.ATTRIBUTE2,
       ASPS.ATTRIBUTE3,
       ASPS.ATTRIBUTE5
  FROM ATET_SB_PAYMENTS_SCHEDULE    ASPS,
       ATET_SB_LOANS                ASL,
       ATET_SB_MEMBERS              ASM,
       ATET_SB_MEMBERS_ACCOUNTS     ASMA   
 WHERE 1 = 1
   AND ASPS.LOAN_ID = ASL.LOAN_ID
   AND ASL.MEMBER_ID = ASM.MEMBER_ID
   AND ASMA.MEMBER_ID = ASM.MEMBER_ID
   AND ASMA.LOAN_ID = ASL.LOAN_ID
   AND ASPS.LOAN_ID IN (505)-- 507
 ORDER BY ASPS.LOAN_ID,
          ASPS.PAYMENT_NUMBER,
          ASPS.PAYMENT_SCHEDULE_ID;
          
