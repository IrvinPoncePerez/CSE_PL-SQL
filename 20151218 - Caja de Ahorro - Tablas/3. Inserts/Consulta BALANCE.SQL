SELECT ASM.EMPLOYEE_NUMBER,
       ASM.EMPLOYEE_FULL_NAME,
       ASM.MEMBER_ID,
       ASL.LOAN_TOTAL_AMOUNT,
       ASL.LOAN_BALANCE,
       ASL.LAST_PAYMENT_DATE,
       ASMA.DEBIT_BALANCE,
       ASMA.CREDIT_BALANCE,
       ASMA.FINAL_BALANCE,
       ASMA.LAST_TRANSACTION_DATE,
       SUM(ASPS.PAYMENT_AMOUNT) PAYMENT_AMOUNT,
       SUM(ASPS.PAYMENT_CAPITAL + ASPS.PAYMENT_INTEREST) PAYMENT_AMOUNT_GL,
       SUM(ASPS.PAYMENT_CAPITAL) PAYMENT_CAPITAL,
       SUM(ASPS.PAYMENT_INTEREST) PAYMENT_INTEREST,
       SUM(ASPS.PAYMENT_INTEREST_LATE) PAYMENT_INTEREST_LATE,
       SUM(ASLT.DEBIT_AMOUNT) DEBIT_AMOUNT,
       SUM(ASLT.CREDIT_AMOUNT) CREDIT_AMOUNT
  FROM ATET_SB_MEMBERS              ASM,
       ATET_SB_LOANS                ASL,
       ATET_SB_MEMBERS_ACCOUNTS     ASMA,
       ATET_SB_PAYMENTS_SCHEDULE    ASPS,
       ATET_SB_LOANS_TRANSACTIONS   ASLT
 WHERE 1 = 1 
   AND ASM.MEMBER_ID = ASL.MEMBER_ID
--   AND ASM.EMPLOYEE_NUMBER IN (41, 45, 50, 52, 82, 1799, 3518)
   AND ASM.MEMBER_ID = ASMA.MEMBER_ID        
   AND ASMA.LOAN_ID = ASL.LOAN_ID
   AND ASPS.LOAN_ID = ASL.LOAN_ID
   AND ASPS.STATUS_FLAG = 'PAYED'
   AND ASLT.MEMBER_ACCOUNT_ID = ASMA.MEMBER_ACCOUNT_ID
   AND ASM.MEMBER_ID = ASLT.MEMBER_ID
   AND ASLT.LOAN_ID = ASL.LOAN_ID
 GROUP BY  ASM.EMPLOYEE_NUMBER,
           ASM.EMPLOYEE_FULL_NAME,
           ASM.MEMBER_ID,
           ASL.LOAN_BALANCE,
           ASL.LOAN_TOTAL_AMOUNT,
           ASL.LAST_PAYMENT_DATE,
           ASMA.DEBIT_BALANCE,
           ASMA.CREDIT_BALANCE,
           ASMA.FINAL_BALANCE,
           ASMA.LAST_TRANSACTION_DATE