--       ASM.ATTRIBUTE7,
--       ASMA.ACCOUNT_NUMBER,
--       ASMA.ACCOUNT_DESCRIPTION,
--       ASMA.DEBIT_BALANCE,
--       ASMA.CREDIT_BALANCE,
--       ASMA.FINAL_BALANCE
 ORDER BY ASM.MEMBER_ID