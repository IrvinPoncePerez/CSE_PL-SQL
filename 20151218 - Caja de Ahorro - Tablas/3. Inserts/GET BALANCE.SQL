SELECT ASM.MEMBER_ID,
       ASM.EMPLOYEE_NUMBER,
       ASM.EMPLOYEE_FULL_NAME,
       ASM.AMOUNT_TO_SAVE,
--       ASM.ATTRIBUTE2,
--       ASM.ATTRIBUTE3,
--       ASM.ATTRIBUTE6,
--       ASM.ATTRIBUTE7,
--       ASMA.ACCOUNT_NUMBER,
--       ASMA.ACCOUNT_DESCRIPTION,
       ASMA.DEBIT_BALANCE,
       ASMA.CREDIT_BALANCE,
       ASMA.FINAL_BALANCE,
       ASST.DEBIT_AMOUNT  DEBIT_AMOUNT,
       ASST.CREDIT_AMOUNT CREDIT_AMOUNT,
       ASST.PERIOD_NAME
  FROM ATET_SB_MEMBERS              ASM,
       ATET_SB_MEMBERS_ACCOUNTS     ASMA,
       ATET_SB_SAVINGS_TRANSACTIONS ASST
 WHERE 1 = 1
   AND ASM.SAVING_BANK_ID = ATET_SAVINGS_BANK_PKG.GET_SAVING_BANK_ID
   AND ASM.MEMBER_ID = ASMA.MEMBER_ID
   AND ASST.MEMBER_ID = ASM.MEMBER_ID
   AND ASST.MEMBER_ACCOUNT_ID = ASMA.MEMBER_ACCOUNT_ID
   
-- GROUP BY ASM.MEMBER_ID,
--       ASM.EMPLOYEE_NUMBER,
--       ASM.EMPLOYEE_FULL_NAME,
--       ASM.AMOUNT_TO_SAVE,
--       ASM.ATTRIBUTE2,
--       ASM.ATTRIBUTE3,
