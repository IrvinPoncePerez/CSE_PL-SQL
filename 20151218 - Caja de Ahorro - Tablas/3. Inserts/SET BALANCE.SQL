SELECT ASM.MEMBER_ID,
       ASM.PERSON_ID, 
       ASL.LOAN_ID,
       ASL.LOAN_AMOUNT
       
  FROM ATET_SB_MEMBERS      ASM,
       ATET_SB_LOANS        ASL,
       ATET_SB_MEMBERS_ACCOUNTS ASMA
 WHERE 1 = 1
   AND ASM.MEMBER_ID = ASL.MEMBER_ID
   AND ASM.MEMBER_ID = ASMA.MEMBER_ID;
   
  
BEGIN
  ATET_SAVINGS_BANK_PKG.SET_LOAN_BALANCE(33, 8000, 1157); 
END;