SELECT DISTINCT
       EMPLOYEE_NUMBER,
       FULL_NAME,
       PAYROLL_NAME
  FROM PER_PEOPLE_F         PPF,
       PER_ASSIGNMENTS_F    PAF,
       PAY_PAYROLLS_F       PPF
 WHERE 1 = 1
   AND PPF.PERSON_ID = PAF.PERSON_ID
   AND PAF.PAYROLL_ID = PPF.PAYROLL_ID
   AND PPF.PAYROLL_NAME = '02_QUIN - CONFIDENCIAL'
   
 ORDER BY TO_NUMBER(EMPLOYEE_NUMBER)