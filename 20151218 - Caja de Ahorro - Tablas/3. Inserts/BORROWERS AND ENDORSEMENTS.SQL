SELECT ASM.EMPLOYEE_NUMBER,
       ASM.EMPLOYEE_FULL_NAME,
       ASM.IS_SAVER,
       ASM.IS_BORROWER,
       ASM.IS_ENDORSEMENT,
       ASL.LOAN_ID,
       ASL.LOAN_BALANCE,
       ASL.LOAN_STATUS_FLAG,
       ASL.TERM_PERIODS,
       ASM2.EMPLOYEE_NUMBER,
       ASM2.EMPLOYEE_FULL_NAME,
       ASM2.IS_SAVER,
       ASM2.IS_BORROWER,
       ASM2.IS_ENDORSEMENT
  FROM ATET_SB_MEMBERS      ASM,
       ATET_SB_LOANS        ASL,
       ATET_SB_ENDORSEMENTS ASE,
       ATET_SB_MEMBERS      ASM2
 WHERE 1 = 1
   AND ASM.MEMBER_ID = ASL.MEMBER_ID
   AND ASL.LOAN_ID = ASE.LOAN_ID
   AND ASE.MEMBER_ENDORSEMENT_ID = ASM2.MEMBER_ID;
   
   




