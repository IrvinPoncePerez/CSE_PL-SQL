SELECT A.FLEX_VALUE,
       SUBSTR(C.DESCRIPTION,0,25)
  FROM FND_FLEX_VALUES A, FND_FLEX_VALUE_SETS B, FND_FLEX_VALUES_TL C
 WHERE 1 = 1
   AND A.FLEX_VALUE_SET_ID = B.FLEX_VALUE_SET_ID
   AND B.FLEX_VALUE_SET_NAME LIKE '%FUT2_CALVARIO%'
   AND A.FLEX_VALUE_ID       = C.FLEX_VALUE_ID
   AND C.LANGUAGE            = 'ESA'
 ORDER BY 1
 
 
 


SELECT PERIOD_NAME
  FROM GL_PERIODS
 WHERE 1 = 1
   AND YEAR_START_DATE      IS NOT NULL
  AND QUARTER_START_DATE   IS NOT NULL
  AND PERIOD_SET_NAME = 'CAL_GPO CALVARI'
 ORDER BY PERIOD_YEAR,
          PERIOD_NUM
