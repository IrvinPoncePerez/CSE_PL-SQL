CREATE OR REPLACE TRIGGER ATET_SB_PAYROLL_RESULTS_TGR
BEFORE INSERT
   ON ATET_SB_PAYROLL_RESULTS
   FOR EACH ROW
DECLARE

   var_next_id  NUMBER;

BEGIN

    SELECT ATET_SB_PAYROLL_RESULTS_SEQ.NEXTVAL
      INTO var_next_id
      FROM dual;        
      
    :NEW.PAYROLL_RESULT_ID := var_next_id;

END;